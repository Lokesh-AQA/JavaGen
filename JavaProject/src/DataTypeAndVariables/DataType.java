package DataTypeAndVariables;

public class DataType {
	
	public static void main(String[] args) {
		
		//Short 
		short myNum0 = 5000; 
		System.out.println(myNum0);
		
		//Int
		int myNum1 = 1000000;
		System.out.println(myNum1);
		
		//Byte
		byte myNum2 =100;
		System.out.println(myNum2);
		
		//Long
		long myNum3 = 15000000000L;
		System.out.println(myNum3);
		
		//Float
		float myNum4 = 5.75f;
        System.out.println(myNum4);
        
        //Double
        double myNum5=19.99d;
        System.out.println(myNum5);
        
        //Boolean
        boolean myNum6 = true;
        System.out.println(myNum6);
        
        //Character
        char myNum7= 'B';  // Single quote
        System.out.println(myNum7);
        
        //String
        String str= "Ignite";   
        System.out.println(str);
        // Whatever inside double quote is consider as String Example Numeric or AlphaNumeric or Character.
        
	}

}

// Primitive DataType is "String"
// Non Primitive dataType is Listed Above

